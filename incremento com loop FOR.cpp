#include <iostream>

using namespace std;

int main() {
	int numero1 = 10;
	for(int i = 0; i < 5; i++)
{
	numero1++;
}
	cout << " o numero incrementado e: " << numero1 << endl;
	
	int numero2 = 8;
	for(int i = 0; i < 2; i++)
{
	numero2--;
}
	cout << " o numero decrementado e: " << numero2 << endl;
	return 0;
	
}
