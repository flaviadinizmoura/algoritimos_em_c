#include <iostream>
using namespace std;
int main ()
{
//Algoritimo Soma de 2 Valores
	int valor1 = 20;
	int valor2 = 30;
	int soma = valor1+valor2;
	cout << " a soma de " << valor1 << " e " << valor2 << " e igual a " << soma << endl;
	
//Algoritimo Subtracao de 2 Valores		
	int valor3 = 30;
	int valor4 = 10;
	int subtracao = valor3-valor4;
	cout << " a subtracao de " << valor3 << " e " << valor4 <<" e igual a " << subtracao << endl;
	
//Algoritimo Multiplicacao de 2 Valores
	int valor5 = 2;
	int valor6 = 40;
	int multiplicacao = valor5*valor6;
	cout <<" a multiplicacao de " << valor5 << " e " << valor6 <<" e igual a " << multiplicacao << endl;

//Algoritimo com Media
    int valor7 = 60;
	int valor8 = 70;
	int valor9 = 90;
	int media = (valor7+valor8+valor9) /3.0;
	cout << " a media de " << valor7 << " , " << valor8 << " e " << valor9 << " e igual a " << media << endl;

//Algoritimo de incremento de 10 a 15.
    int valor10 = 10;
    valor10 +=5;
    cout << " o valor incrementado e " << valor10 << endl;	
 
//Algoritimo de decremento de 8 a 6.
    int valor11 = 8;
    valor11 -=2;
	cout << " o valor decrementado e " << valor11 << endl;	
    return 0;
 } 

   
   

